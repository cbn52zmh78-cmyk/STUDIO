"""Sofia Coppola cinematic techniques and prompting language."""


class SofiaCoppola:
    """Signature techniques and prompting patterns for Sofia Coppola style."""

    SIGNATURE_TECHNIQUES = [
        "Negative space & isolation in wide compositions",
        "Soft, pastel, atmospheric lighting",
        "Lingering sensory texture and slow, fluid camera",
    ]

    @staticmethod
    def get_prompting_language() -> str:
        return (
            "Sofia Coppola-style wide composition with generous negative space isolating her in the ornate room, "
            "soft pastel lighting and lens flare, slow drifting camera lingering on fabric texture and her distant expression."
        )
